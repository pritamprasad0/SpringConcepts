package com.pritamprasad.spring_intercepter.model;

import org.springframework.stereotype.Component;

@Component
public class DefaultModel {

	private int id;
	
	private String name;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
